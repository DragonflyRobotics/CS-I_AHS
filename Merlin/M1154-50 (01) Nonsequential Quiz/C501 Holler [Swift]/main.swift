func hollers(from: String, to: String, repeated: Int) {
    print(from + " hollers ", terminator: "")
    var sup = ""
    for i in 1...repeated {
        var punc = ""
        for _ in 1...i {
            punc += "!"
        }
        if(i == repeated) {
            sup += to + punc
        }
        else {
            sup += to + punc + ", "
        }
    }
    print(sup)
}





