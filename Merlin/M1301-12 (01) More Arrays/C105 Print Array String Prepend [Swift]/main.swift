func prependPrevious(words: [String]) -> [String] {
    var new_words: [String] = []
    for n in 0..<words.count {
        if n+1<words.count {
            new_words.append(words[n] + words[n+1])
        }
    }
    return new_words//.map({$0} + {$1})
}

