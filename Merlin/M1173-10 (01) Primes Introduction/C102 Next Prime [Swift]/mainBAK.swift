import Foundation
func isPrime(n: Int) -> Bool {
    if n < 2 {
        return false
    }
    for i in 2..<Int(sqrt(Double(n)))+1 {
        if (n%i) == 0 {
            return false
        }
    }
    return true
}

func nextPrime(n: Int) -> Int {
    var num = n
    repeat {
        num += 1
    } while isPrime(n: num) == false
    return num
}   





