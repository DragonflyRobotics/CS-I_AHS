func applyOperation(integers: [Int], operand: Int, operatorChar: Character) -> [Int] {
    switch operatorChar {
        case "*":
            return integers.map{$0 * operand}
        case "+":
            return integers.map{$0 + operand}
        default:
            return integers
    }
}






